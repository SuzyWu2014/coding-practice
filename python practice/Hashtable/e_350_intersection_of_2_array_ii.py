    def intersect2(self, nums1, nums2):
        """
        :type nums1: List[int]
        :type nums2: List[int]
        :rtype: List[int]
        """
        if len(nums1) > len(nums2):
            nums1, nums2 = nums2, nums1
        count_num = collections.Counter(nums1)
        rst = []
        for num in nums2:
                rst.append(num)
                count_num[num] -= 1
        return rst

